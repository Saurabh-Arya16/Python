marks=int(input("enter the marks::"))

if(marks>=95):
    print("O grade")
elif(marks>=90):
    print("A grade")
elif(marks>=80):
    print("B grade")
elif(marks>=70):
    print("C grade")
elif(marks>=50):
    print("D grade")
elif(marks>=40):
    print("E grade")

else:
    print("F grade")    